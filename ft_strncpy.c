#include "libft.h"

char	*ft_strncpy(char *dst, const char * src, size_t n)
{
	size_t	p;
	
	p = 0;
	while (p < n && src[p])
	{
		dst[p] = src[p];
		p++;
	}
	while (p < n)
	{
		dst[p] = '\0';
		p++;
	}
	return (dst);
}
